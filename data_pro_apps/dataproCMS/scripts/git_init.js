async function main() {
    log('\n🚀 Git Repository Initialization Script', colors.blue);
    log('======================================\n', colors.blue);
    
    // Check if git is installed
    if (!checkGitInstalled()) {
        log('❌ Git is not installed. Please install git first.', colors.red);
        process.exit(1);
    }
    
    const projectName = getCurrentProjectName();
    log(`📁 Current Project: ${projectName}`, colors.cyan);
    
    // Check if already a git repository
    const isGitRepo = checkIfGitRepo();
    if (isGitRepo) {
        log('⚠️  This directory is already a git repository!', colors.yellow);
        const answer = await promptUser('Do you want to reinitialize? (y/N): ');
        if (answer.toLowerCase() !== 'y') {
            log('❌ Operation cancelled', colors.red);
            rl.close();
            return;
        }
        
        // Remove existing git
        try {
            fs.rmSync('.git', { recursive: true, force: true });
            log('✅ Removed existing git repository', colors.green);
        } catch (error) {
            log('❌ Failed to remove existing git repository', colors.red);
            rl.close();
            return;
        }
    }
    
    // Confirm repository name
    log(`\n📦 Repository will be created as: ${colors.yellow}amatak-cloud/${projectName}${colors.reset}`);
    const confirm = await promptUser('Is this correct? (Y/n): ');
    
    let finalProjectName = projectName;
    if (confirm.toLowerCase() === 'n') {
        finalProjectName = await promptUser('Enter repository name: ');
    }
    
    // Create .gitignore
    await createGitIgnore();
    
    // Execute git commands
    log('\n🚀 Executing git commands...', colors.blue);
    
    const commands = [
        { cmd: 'git init', desc: 'Git init' },
        { cmd: 'git add .', desc: 'Git add' },
        { cmd: 'git commit -m "first commit"', desc: 'Git commit' },
        { cmd: 'git branch -M main', desc: 'Rename branch to main' },
        { cmd: `git remote add origin https://github.com/amatak-cloud/${finalProjectName}.git`, desc: 'Add remote origin' }
    ];
    
    for (const command of commands) {
        const success = executeCommand(command.cmd, command.desc);
        if (!success) {
            log('\n❌ Git initialization failed!', colors.red);
            rl.close();
            return;
        }
    }
    
    // Ask about push
    log('\n', colors.reset);
    const pushAnswer = await promptUser('Do you want to push to remote now? (Y/n): ');
    
    if (pushAnswer.toLowerCase() !== 'n') {
        const pushSuccess = executeCommand('git push -u origin main', 'Git push');
        if (pushSuccess) {
            log('\n✅ Repository successfully initialized and pushed!', colors.green);
            log(`🔗 Remote URL: https://github.com/amatak-cloud/${finalProjectName}.git`, colors.cyan);
        }
    } else {
        log('\n✅ Repository initialized locally. Push manually with:', colors.yellow);
        log('   git push -u origin main', colors.cyan);
    }
    
    // Show status
    log('\n📊 Current Git Status:', colors.blue);
    try {
        execSync('git status', { stdio: 'inherit' });
    } catch (error) {
        // Ignore status errors
    }
    
    log('\n✨ Git initialization complete!', colors.green);
    rl.close();
}

// Handle Ctrl+C
process.on('SIGINT', () => {
    log('\n\n❌ Operation cancelled by user', colors.red);
    rl.close();
    process.exit(0);
});

// Run the script
if (require.main === module) {
    main().catch(error => {
        log(`\n❌ Unexpected error: ${error.message}`, colors.red);
        rl.close();
        process.exit(1);
    });
}

module.exports = { main };