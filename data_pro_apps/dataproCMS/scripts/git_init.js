const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');
const readline = require('readline');

const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout
});

// Colors for console output
const colors = {
    reset: '\x1b[0m',
    green: '\x1b[32m',
    yellow: '\x1b[33m',
    red: '\x1b[31m',
    blue: '\x1b[34m',
    cyan: '\x1b[36m'
};

function log(message, color = colors.reset) {
    console.log(`${color}${message}${colors.reset}`);
}

function executeCommand(command, description) {
    try {
        log(`\n▶ ${description}...`, colors.cyan);
        execSync(command, { stdio: 'inherit' });
        log(`✅ ${description} completed successfully!`, colors.green);
        return true;
    } catch (error) {
        log(`❌ Failed to ${description.toLowerCase()}`, colors.red);
        log(`Error: ${error.message}`, colors.red);
        return false;
    }
}

function getCurrentProjectName() {
    return path.basename(process.cwd());
}

function checkGitInstalled() {
    try {
        execSync('git --version', { stdio: 'ignore' });
        return true;
    } catch (error) {
        return false;
    }
}

function checkIfGitRepo() {
    try {
        execSync('git rev-parse --git-dir', { stdio: 'ignore' });
        return true;
    } catch (error) {
        return false;
    }
}

function getRemoteUrl(projectName) {
    return `https://github.com/amatak-cloud/${projectName}.git`;
}

function promptUser(question) {
    return new Promise((resolve) => {
        rl.question(question, (answer) => {
            resolve(answer);
        });
    });
}

